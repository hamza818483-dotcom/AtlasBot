def pick_feedback(correct: int, total: int) -> tuple:
    """Return (motivation_message, random_ayat) based on exam score."""
    pct = (correct / total * 100) if total else 0
    if pct >= 80:
        msg = random.choice([
            '🏆 অসাধারণ! অনেক ভালো করেছো!',
            '🌟 দারুণ! তুমি সত্যিই প্রস্তুত!',
            '💪 বাহ! চমৎকার ফলাফল!',
        ])
    elif pct >= 60:
        msg = random.choice([
            '✅ মোটামুটি ভালো! চেষ্টা চালিয়ে যাও!',
            '👍 ভালো হয়েছে! আরেকটু উন্নতি করতে পারবে!',
        ])
    elif pct >= 40:
        msg = random.choice([
            '📚 আরো পড়তে হবে! হাল ছেড়ো না!',
            '💭 ঠিক আছে, নিয়মিত চর্চা করো!',
        ])
    else:
        msg = random.choice([
            '💪 পড়া হয়নি! আবার পড়ে practice করো!',
            '🌱 শুরুটা কঠিনই হয়! লেগে থাকো!',
        ])
    return msg, random.choice(_AYATS)
